class WeatherData {
	constructor(jsonData) {
		const data = this.#formatData(jsonData);

		this.location = data.location;
		this.description = data.description;
		this.currentConditions = data.currentConditions;
		this.days = data.days;
		this.alerts = data.alerts;
		this.timezone = data.timezone;
	}

	#formatData(data) {
		return {
			location: data.resolvedAddress,
			description: data.description,
			currentConditions: new Day(data.currentConditions),
			days: data.days.map((jsonData) => new Day(jsonData)),
			alerts: data.alerts,
			timezone: data.timezone,
		};
	}
}

class Day {
	constructor(jsonData) {
		const data = this.#formatData(jsonData);

		this.time = data.time;
		this.conditions = data.conditions;
		this.temp = data.temp;
		this.feelsLike = data.feelsLike;
		this.humidity = data.humidity;
		this.windSpeed = data.windSpeed;
		this.windGust = data.windGust;
		this.windDir = data.windDir;
		this.uvIndex = data.uvIndex;
		this.snow = data.snow;
		this.sunrise = data.sunrise;
		this.sunset = data.sunset;
		this.icon = data.icon;
	}

	#formatData(data) {
		return {
			time: data.datetime,
			conditions: data.conditions,
			temp: Math.round(Number((data.temp - 32) / 1.8)),
			feelsLike: Math.round(Number((data.feelslike - 32) / 1.8)),
			humidity: data.humidity,
			windSpeed: Math.round(data.windspeed),
			windGust: data.windgust,
			windDir: data.winddir,
			uvIndex: data.uvindex,
			snow: data.snow,
			sunrise: data.sunrise,
			sunset: data.sunset,
			icon: `./img/${data.icon}.svg`,
		};
	}
}

async function requestWeatherData(location) {
	const requestString = `https://weather.visualcrossing.com/VisualCrossingWebServices/rest/services/timeline/${location}?key=VK2YN8PQWPJW6MM627KLFY4WC`;

	const response = await fetch(requestString, {
		mode: 'cors',
	});

	if (!response.ok) {
		const error = `Failed api call: status code ${response.status}`;
		PubSub.publish('WEATHER-DATA-ERROR', error);
	}

	PubSub.publish(
		'WEATHER-DATA-UPDATED',
		new WeatherData(await response.json()),
	);
}

